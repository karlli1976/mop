fn main() {
  // Ensure a minimal icon exists for Tauri to bundle, to avoid build errors
  // when `src-tauri/icons/icon.png` is missing. We generate a 1x1 PNG on the fly.
  use std::path::PathBuf;
  let mut icon_path = PathBuf::from(env!("CARGO_MANIFEST_DIR"));
  icon_path.push("icons");
  let _ = std::fs::create_dir_all(&icon_path);
  icon_path.push("icon.png");

  if !icon_path.exists() {
    // 1x1 transparent PNG bytes (PNG signature + IHDR + IDAT + IEND)
    // Equivalent to base64: iVBORw0KGgoAAAANSUhEUgAAAAEAAAABCAQAAAC1HAwCAAAAC0lEQVR42mP8/x8AAwMCAO2WqX8AAAAASUVORK5CYII=
    const ICON_BYTES: &[u8] = &[
      0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A, 0x00, 0x00, 0x00, 0x0D, 0x49, 0x48, 0x44, 0x52,
      0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01, 0x08, 0x02, 0x00, 0x00, 0x00, 0x90, 0x77, 0x53,
      0xDE, 0x00, 0x00, 0x00, 0x0A, 0x49, 0x44, 0x41, 0x54, 0x78, 0x9C, 0x63, 0xF8, 0xFF, 0x1F, 0x00,
      0x03, 0x03, 0x02, 0x00, 0xED, 0x96, 0xA9, 0x7F, 0x00, 0x00, 0x00, 0x00, 0x49, 0x45, 0x4E, 0x44,
      0xAE, 0x42, 0x60, 0x82,
    ];
    let _ = std::fs::write(&icon_path, ICON_BYTES);
  }

  tauri_build::build()
}
